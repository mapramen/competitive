bool Solve(){
  int n;
  scanf("%d", &n);

  for(int i = 1; i <= n; ++i){
    scanf("%d", &a[i]);
  }

  sort(a + 1, a + n + 1);
  for(int i = 2; i <= n; ++i){
    if(a[i] == a[i - 1]){
      return true;
    }
  }

  return false;
}

