#include <bits/stdc++.h>

using namespace std;

typedef long long ll;

#define pii pair<int,int>
#define pll pair<ll,ll>

int Solve(){
  int n;
  scanf("%d", &n);

  ll p = 1;
  for( ; p <= n; p *= 10);
  p /= 10;

  return n - p;
}

