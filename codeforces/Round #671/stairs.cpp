int Solve(){
  ll n;
  scanf("%lld", &n);

  int ans = 0;
  ll k = 1;
  for( ; k * (k + 1) <= 2 * n; k = 2 * k + 1){
    n -= (k * (k + 1) / 2);
    ++ans;
  }

  return ans;
}

